        <h1>About Us</h1>
    </a>
    <p>DocuTracer is an integrated system that helps find and match lost documents with their rightful owners.
    It provides you with an online platform to back up information / scanned copies of your documents such as: 
    certificates,National/School/Work ID,ATM Cards, among others. Also, it allows for organizations and 
    institutions to use a desktop based software that enables tracking of their students/workers lost documents, 
    within work/school premises.</p>
    
    <h2>Our Aim</h2>
    <br>
    <p>The aim of the system is to find lost documents in a more accurate, easier and faster way, through;</p>
    <ul>
        <li>significant reduction in amount of time spend searching for lost documents.</li>
        <li>pin point accuracy in matching of found documents against lost documents.</li>
        <li>pin point accuracy in matching of found documents against their rightful owners.</li>
        <li>provision of real time notifications to users and document owners.</li>
    </ul>
    
    <h2>How it works</h2>
    <br>
    <p>After you have registered, you can do the following;</p>
    <ul>
        <li>Back up your information of your documents, including scanned copies.</li>
        <li>Mark your documents as either lost or found.</li>
        <li>Report found documents</li>
    </ul>
    <p>In case of a lost or found document, the system;</p>
    <ul>
        <li>Sends you a notification of approval, if you marked your documents as lost.</li>
        <li>Displays all lost / found documents on a public page, for easier finding.</li>
        <li>Matches information of found documents against the registered ones (to determine thier rightful owners), 
            and sends you (the owner) a notification, if your match is found.</li>
    </ul>
